    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
         if (jComboBox1.getSelectedItem() != null)
         {
        int id = Integer.parseInt(((String) jComboBox1.getSelectedItem()).split(" ")[0]);
        film = filmDao.getFilm(id);
        System.out.println("c'est l'id n°"+ film.getId());
       System.out.println("le titre"+ film.getTitre());

        /*
         * recuperer les champs de chaque film selectionné
         */
         clear();
             this.jTextField1.setText(film.getTitre());
        // reset durée
        Time duration= film.getDuree();
        DateFormat dateFormat = new SimpleDateFormat("hh:mm");

        this.jTextField2.setText(dateFormat.format(duration));
        // reset date de sortie
        Date date = film.getDateSortie();
        


        this.jTextField3.setText(date.toString());
        // reset acteurs
        this.jTextField4.setText(film.getActeur());
        // reset resume
        this.jTextArea1.setText(film.getResume());
        // reset popularite
        float pop = film.getPopularite();

        this.jTextField6.setText(String.valueOf(pop));
        System.out.println(film.getImage());
        try {
            // charger l'image du film
            jLabel3.setIcon(new ImageIcon(ImageIO.read(new File("./tools/images/" + film.getImage()))));
        } catch (IOException ex) {
           JOptionPane.showMessageDialog(null, "la liste des films est vide!");
        }

         
        }


    }//GEN-LAST:event_jComboBox1ActionPerformed
