    private void updateFilm() throws ParseException {
            film.setTitre(jTextField1.getText());

            // convertir le type String to Time
            String dure;
            String[] nombre;

            dure = jTextField2.getText();
            nombre = dure.split(":");
            film.setDuree(new Time(
                    Integer.parseInt(nombre[0]),
                    Integer.parseInt(nombre[1]), 0));

            String date = jTextField3.getText();
            nombre = date.split("-");
            film.setDateSortie(new Date(Integer.parseInt(nombre[0]),
                    Integer.parseInt(nombre[1]),
                    Integer.parseInt(nombre[2])));

            //DateFormat dates = new SimpleDateFormat("yyyy-mm-dd");
            // recupere la date de sortie de film
            //dateSortie =  (Date) dates.
            film.setActeur(jTextField4.getText());
            film.setResume(jTextArea1.getText());

            String popular = jTextField6.getText();
            // convertir le string en float
            film.setPopularite(Float.valueOf(popular.trim()).floatValue());

            if (filmDao.updateFilm(film))
            {
                refresh();
                
            JOptionPane.showMessageDialog(null,
                    "le film est bien modifié.");
        } else {
            JOptionPane.showMessageDialog(null, "erreur, le film n'est pas modifié");
        }// </editor-fold>









    }
    // effacer un film de la base

    private void removeFilm() {

        titre = jTextField1.getText();
        titre = titre.toUpperCase();
        if (titre.equals("")) {
            JOptionPane.showMessageDialog(null,
                    "sélectionner un film à supprimer.");
        } else {
            //effacer le film afficher dans la liste par le bouton effacer
            boolean ok = filmDao.removeFilm(film.getId());
            if (ok) {
                JOptionPane.showMessageDialog(null, " Film effacer.");
                refresh();
            } else {
                JOptionPane.showMessageDialog(null, " Film non effacer.");

            }


        }











    }
